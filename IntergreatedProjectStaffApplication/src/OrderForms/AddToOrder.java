    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        boolean validation;
        
        validation = cboIngredient.getSelectedIndex() > -1 && Utilities.Utilities.numericDecimal(txtAmount.getText(), "Amount", "Amount") &&
                Utilities.Utilities.nullCheck(txtAmount, "Amount", "Amount");
        
        if (validation) {
            OrderStock stock = new OrderStock(ingredients.get(cboIngredient.getSelectedIndex()).getIngredientID(), Integer.parseInt(txtAmount.getText()));
            Utilities.IntergratedProjectStaffApplication.addEditOrder.addToOrder(stock,
                    ingredients.get(cboIngredient.getSelectedIndex()).getName(), ingredients.get(cboIngredient.getSelectedIndex()).getCostPerUnit() );
            
            txtAmount.setText("");
            cboIngredient.setSelectedIndex(0);
        }
    }//GEN-LAST:event_btnAddActionPerformed

