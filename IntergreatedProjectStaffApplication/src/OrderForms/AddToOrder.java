    public void setupForm() {
        ArrayList<Ingredient> allIngredients = Utilities.IntergratedProjectStaffApplication.JSPConnector.getAllIngredient();
        for (Ingredient ingredient: allIngredients) {
            if (ingredient.getSupplierID() == supplier.getSupplierID()) {
                ingredients.add(ingredient);
                cboIngredient.addItem(ingredient.getName());
            }            
        }
    }

