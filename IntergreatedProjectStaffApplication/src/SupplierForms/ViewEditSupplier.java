    public void formSetup() {        
        originalSupplier = Utilities.IntergratedProjectStaffApplication.JSPConnector.getAllSupplier();
        for (Supplier supplier : originalSupplier) //copy arrayList
            searchedSupplier.add(supplier);        
        listLoad();
        if (!searchedSupplier.isEmpty()) {
            lstSupplier.setSelectedIndex(0);
            setCurrentSupplierBoxs();
        }
        setEventListeners();
    }
    
    public void listLoad() {
        
        lstSupplier.removeAll();
        
        if (!searchedSupplier.isEmpty()) {
            lstSupplierModel = new ArrayList<>();        
            String[] supplierListArray;
            for (Supplier supplier : searchedSupplier) {
                lstSupplierModel.add(supplier.getSupplierName());
            }
            supplierListArray = lstSupplierModel.toArray(new String[0]);
            lstSupplier.setListData(supplierListArray); //set list    
        }
    }
    
    public void setEventListeners() {
        lstSupplier.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!searchActivated) //this check only allows the action to take place when manually changed,
                    setCurrentSupplierBoxs(); // not when the list repopulates calling the listener
                else
                    searchActivated = false;
                currentSelection = lstSupplier.getSelectedIndex();
            }
        });
    }
    
    public void setCurrentSupplier() {
        currentSupplier.setSupplierName(txtName.getText());
        currentSupplier.setAddress1(txtAddress1.getText());
        currentSupplier.setAddress2(txtAddress2.getText());
        currentSupplier.setCity(txtCity.getText());
        currentSupplier.setPostcode(txtPostcode.getText());
        currentSupplier.setPhoneNo(txtPhoneNo.getText());
        currentSupplier.setEmail(txtEmail.getText());
    }
    
    public void setCurrentSupplierBoxs() {
        //get current selected supplier and set boxs
        currentSupplier = searchedSupplier.get(lstSupplier.getSelectedIndex());
        txtName.setText(currentSupplier.getSupplierName());
        txtAddress1.setText(currentSupplier.getAddress1());
        txtAddress2.setText(currentSupplier.getAddress2());
        txtCity.setText(currentSupplier.getCity());
        txtPostcode.setText(currentSupplier.getPostcode());
        txtPhoneNo.setText(currentSupplier.getPhoneNo());
        txtEmail.setText(currentSupplier.getEmail());
    }
    
