    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        boolean validation;
        
        validation = Utilities.Utilities.nullCheck(txtName, "Name", "Name") && Utilities.Utilities.alphbetic(txtName.getText(), "Name", "Name") &&
                Utilities.Utilities.nullCheck(txtAddress1, "Address 1", "Address 1") && Utilities.Utilities.alphanumeric(txtAddress1.getText(), "Address 1", "Address 1") &&
                Utilities.Utilities.nullCheck(txtAddress2, "Address 2", "Address 2") && Utilities.Utilities.alphanumeric(txtAddress2.getText(), "Address 2", "Address 2") &&
                Utilities.Utilities.nullCheck(txtCity, "City", "City") && Utilities.Utilities.alphbetic(txtCity.getText(), "City", "City") &&
                Utilities.Utilities.nullCheck(txtPostcode, "Postcode", "Postcode") && Utilities.Utilities.lengthCheckerGreater(txtPostcode, 7, "Postcode", "Postcode") &&
                Utilities.Utilities.lengthCheckerLess(txtPostcode, 8, "Postcode", "Postcode") &&
                Utilities.Utilities.phoneNoCheck(txtPhoneNo.getText()) &&
                Utilities.Utilities.nullCheck(txtEmail, "Email", "Email") && Utilities.Utilities.emailCheck(txtEmail);
        
        if (validation) {
            setCurrentSupplier();
            IntergratedProjectStaffApplication.JSPConnector.updateSupplier(currentSupplier);
            
            searchActivated = true;
            listLoad();
            lstSupplier.setSelectedIndex(currentSelection);
            
            Utilities.Utilities.infoBox("Supplier Updated : " + currentSupplier.getSupplierName(), "Supplier Updated");
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure you wish to remove " + currentSupplier.getSupplierName() +
                "? Removing the supplier will remove all of their ingredients and any meal ingredients associated with them." ,"Remove Supplier", JOptionPane.YES_NO_OPTION);
        if(dialogResult == JOptionPane.YES_OPTION){
            
            IntergratedProjectStaffApplication.JSPConnector.deleteSupplier(currentSupplier);
            
            originalSupplier.remove(currentSupplier);
            searchedSupplier.remove(currentSupplier);
            
            searchActivated = true;
            listLoad();
            
            Utilities.Utilities.infoBox("Supplier removed : " + currentSupplier.getSupplierName(), "Supplier Removed");
            lstSupplier.setSelectedIndex(0);
            }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        
        searchActivated = true;
        searchedSupplier.clear();
        
        String name = txtSearchName.getText();
        String city = txtSearchCity.getText();
        String postcode = txtSearchPostcode.getText();
        
        for (Supplier supplier : originalSupplier) {
            if ((name.isEmpty() || name.equalsIgnoreCase(supplier.getSupplierName())) && (city.isEmpty() || city.equalsIgnoreCase(supplier.getCity())) &&
                    (postcode.isEmpty() || postcode.equalsIgnoreCase(supplier.getPostcode()))) {
                searchedSupplier.add(supplier);
            }
        }        
        
        if (searchedSupplier.isEmpty()) {
            for (Supplier supplier : originalSupplier) {
                searchedSupplier.add(supplier);
            }
            txtSearchName.setText("");
            txtSearchCity.setText("");
            txtSearchPostcode.setText("");
            Utilities.Utilities.infoBox("No matching results - Resetting List", "Search results");
        }
        
       listLoad();
       lstSupplier.setSelectedIndex(0);
    }//GEN-LAST:event_btnSearchActionPerformed

