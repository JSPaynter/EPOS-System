    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        
        boolean validation;
        
        validation = Utilities.Utilities.nullCheck(txtFirstName, "First name", "First Name") && Utilities.Utilities.alphbetic(txtFirstName.getText(), "First Name", "First Name") &&
                Utilities.Utilities.nullCheck(txtSurname, "Surname", "Surname") && Utilities.Utilities.alphanumeric(txtSurname.getText(), "Surname", "Surname") &&
                Utilities.Utilities.nullCheck(txtEmail, "Email", "Email") && Utilities.Utilities.emailCheck(txtEmail) &&
                Utilities.Utilities.nullCheck(txtPhoneNo, "Phone number", "Phone Number") && Utilities.Utilities.phoneNoCheck(txtPhoneNo.getText());
        
        if (validation) {
            setCurrentStaff(); //update details
            IntergratedProjectStaffApplication.JSPConnector.updateStaff(currentStaff);
            
            searchActivated = true;
            listLoad();
            lstStaff.setSelectedIndex(currentSelection);
            
            Utilities.Utilities.infoBox( currentStaff.getFirstName() + " " + currentStaff.getSurname() + " Updated", "Staff Updated" );
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        
        searchActivated = true;
        
        //clear search array for results
        searchedStaff.clear();
        
        //get search values
        String searchFirstName = txtSearchFirstName.getText();
        String searchSurname = txtSearchSurname.getText();
        
        //search
        if (!searchFirstName.isEmpty() && !searchSurname.isEmpty() && selectedRad == 2) {    //search both boxs all staff        
            for (Staff staff : originalStaff) {
                if (staff.getFirstName().equalsIgnoreCase(searchFirstName) && staff.getSurname().equalsIgnoreCase(searchSurname)) {
                    searchedStaff.add(staff);
                }
            }
        } else if (!searchFirstName.isEmpty() && !searchSurname.isEmpty() && selectedRad != 2) { //search both boxs half staff
            for (Staff staff : originalStaff) {
                if (staff.getFirstName().equalsIgnoreCase(searchFirstName) && staff.getSurname().equalsIgnoreCase(searchSurname) && staff.getManager() == selectedRad) {
                    searchedStaff.add(staff);
                }
            }
        } else if (!searchFirstName.isEmpty() && selectedRad == 2) { //search first name only all staff
            for (Staff staff : originalStaff) {
                if (staff.getFirstName().equalsIgnoreCase(searchFirstName)) {
                    searchedStaff.add(staff);
                }
            }
        } else if (!searchFirstName.isEmpty() && selectedRad != 2) { //search first name only half staff
            for (Staff staff : originalStaff) {
                if (staff.getFirstName().equalsIgnoreCase(searchFirstName) && staff.getManager() == selectedRad) {
                    searchedStaff.add(staff);
                }
            }
        } else if (!searchSurname.isEmpty() && selectedRad == 2) { //search surname only all staff            
            for (Staff staff : originalStaff) {
                if (staff.getSurname().equalsIgnoreCase(searchSurname)) {
                    searchedStaff.add(staff);
                }
            }
        } else if (!searchSurname.isEmpty() && selectedRad != 2) { //search surname only half staff
            for (Staff staff : originalStaff) {
                if (staff.getSurname().equalsIgnoreCase(searchSurname) && staff.getManager() == selectedRad) {
                    searchedStaff.add(staff);
                }
            }
        }
        
        //display message for no results
        if (searchedStaff.isEmpty()) {
            for (Staff staff : originalStaff) {
                searchedStaff.add(staff);
            }
            txtSearchFirstName.setText("");
            txtSearchSurname.setText("");
            Utilities.Utilities.infoBox("No matching results - Resetting List", "Search results");
        }
        listLoad();
        lstStaff.setSelectedIndex(0);
    }//GEN-LAST:event_btnSearchActionPerformed

