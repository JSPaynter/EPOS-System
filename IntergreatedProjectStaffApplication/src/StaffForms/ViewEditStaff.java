    public void formSetup() {        
        originalStaff = Utilities.IntergratedProjectStaffApplication.JSPConnector.getAllStaff();
        for (Staff staff : originalStaff) //copy arrayList
            searchedStaff.add(staff);        
        listLoad();
        lstStaff.setSelectedIndex(0);
        setCurrentStaffBoxs();        
        setEventListeners();
    }
    
    public void listLoad() {
        
        lstStaff.removeAll();
        
        lstStaffModel = new ArrayList<>();
                
        if (!searchedStaff.isEmpty()) {
            for (Staff staff : searchedStaff)
                lstStaffModel.add(staff.getFirstName() + " " + staff.getSurname());
        }
        lstStaff.setListData(lstStaffModel.toArray(new String[0])); //set list
    }
    
    public void setEventListeners() {
        lstStaff.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!searchActivated) //this check only allows the action to take place when manually changed,
                    setCurrentStaffBoxs(); // not when the list repopulates calling the listener
                else
                    searchActivated = false;
                currentSelection = lstStaff.getSelectedIndex();
            }
        });
    }
    
    public void setCurrentStaff() {
        currentStaff.setFirstName(txtFirstName.getText());
        currentStaff.setSurname(txtSurname.getText());
        currentStaff.setPhoneNo(txtPhoneNo.getText());
        currentStaff.setEmail(txtEmail.getText());
        if (cbManager.isSelected())
            currentStaff.setManager(1);
        else currentStaff.setManager(0);
    }
    
    public void setCurrentStaffBoxs() {
        //get current selected staff and set boxs
        currentStaff = searchedStaff.get(lstStaff.getSelectedIndex());
        txtFirstName.setText(currentStaff.getFirstName());
        txtSurname.setText(currentStaff.getSurname());
        txtPhoneNo.setText(currentStaff.getPhoneNo());
        txtEmail.setText(currentStaff.getEmail());
        if (currentStaff.getManager() == 1)
            cbManager.setSelected(true);
        else
            cbManager.setSelected(false);
    }
    
    public void clearTextBox() {
        txtFirstName.setText("");
        txtSurname.setText("");
        txtPhoneNo.setText("");
        txtEmail.setText("");
        cbManager.setSelected(false);
        txtSearchFirstName.setText("");
        txtSearchSurname.setText("");
        radBoth.setSelected(true);
    }
    
    public void addNewStaffList(Staff newStaff) {
        lstStaffModel.add(newStaff.getFirstName() + " " + newStaff.getSurname());
        searchedStaff.add(newStaff);
        originalStaff.add(newStaff);
        lstStaff.setListData(lstStaffModel.toArray(new String[0])); //set list
    }
    

