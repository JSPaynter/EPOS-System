    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure you wish to remove " + currentStaff.getFirstName() + " " + currentStaff.getSurname(),"Remove Staff", JOptionPane.YES_NO_OPTION);
        if(dialogResult == JOptionPane.YES_OPTION){
            
            IntergratedProjectStaffApplication.JSPConnector.deleteStaff(currentStaff);
            
            originalStaff.remove(currentStaff);
            searchedStaff.remove(currentStaff);            
            
            searchActivated = true;
            listLoad();            
            Utilities.Utilities.infoBox("Staff removed : " + currentStaff.getFirstName() + " " + currentStaff.getSurname() , "Staff Removed");
            lstStaff.setSelectedIndex(0);
            }
    }//GEN-LAST:event_btnRemoveActionPerformed

