    public void setupForm() {
        ingredients = Utilities.IntergratedProjectStaffApplication.JSPConnector.getAllIngredient();
        for (Ingredient ingredient : ingredients) {
            ingredientStrings.add(ingredient.getName());
        }
        produce = Utilities.IntergratedProjectStaffApplication.JSPConnector.getAllProduce();
        for (MadeProduce item : produce) {
            produceStrings.add(item.getName());
        }
        comboBoxFill(ingredientStrings);
    }
    
    public void comboBoxFill(ArrayList<String> lines) {        
        cboItem.removeAllItems();
        for (String line : lines) {
            cboItem.addItem(line);
        }        
    }
    
    public void clearForm() {
        cboItem.setSelectedIndex(0);
        txtAmount.setText("");
    }

