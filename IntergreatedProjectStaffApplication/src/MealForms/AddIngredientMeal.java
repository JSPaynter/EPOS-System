    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       
        boolean validation;
        
        validation = Utilities.Utilities.nullCheck(txtAmount, "Amount", "Amount") &&
                        Utilities.Utilities.numericDecimal(txtAmount.getText(), "Amount", "Amount");
        
        if (validation) {
            int ingredientID = 0;
            int produceIngredient = 0;
            double amount = 0.00;
            String name;
            
            if (radIngredient.isSelected()) {
                produceIngredient = 1;                    
                ingredientID = ingredients.get(cboItem.getSelectedIndex()).getIngredientID();
            } else if (radProduce.isSelected()) {
                produceIngredient = 0;
                ingredientID = produce.get(cboItem.getSelectedIndex()).getMadeProduceID();
            }            
            name = txtAmount.getText() + " : " + cboItem.getSelectedItem().toString();
            amount += Double.parseDouble(txtAmount.getText());
            MealIngredient mealIngredient = new MealIngredient(ingredientID, produceIngredient, amount);            
            switch (addOrEdit) {
                    case 1: //edit                        
                        Utilities.IntergratedProjectStaffApplication.viewEditMeal.addIngredient(mealIngredient, name);
                        break;
                    case 0: //add                        
                        Utilities.IntergratedProjectStaffApplication.addMeal.addIngredient(mealIngredient, name);
                        break;
            }
            Utilities.Utilities.infoBox("Item Added : " + cboItem.getSelectedItem(), "Ingredient Added");
            clearForm();
        }
    }//GEN-LAST:event_btnAddActionPerformed

